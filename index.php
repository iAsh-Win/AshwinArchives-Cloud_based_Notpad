  <script>
    document.addEventListener('DOMContentLoaded', () => {
      const yearSpan = document.getElementById('current-year');
      const currentYear = new Date().getFullYear();
      yearSpan.textContent = currentYear;
      const usernameInput = document.getElementById('username');
      const passwordInput = document.getElementById('password');
      const form = document.getElementById('f1');
      const goButton = document.getElementById('go-btn1');
      const d1 = document.getElementById('d1');
      const d2 = document.getElementById('d2');
      const txt = document.getElementById('txt');
      let userExist;

      // Function to show/hide preloader
      function togglePreloader(show) {
        const preloader = document.getElementById('preloader');
        if (show) {
          if (!preloader) {
            const newPreloader = document.createElement('div');
            newPreloader.id = 'preloader';
            document.body.appendChild(newPreloader);
          }
        } else {
          if (preloader) {
            document.body.removeChild(preloader);
          }
        }
      }

      // Function to validate input
      function validateInput(input, type) {
        const value = input.value.trim();
        if (value === '' || /\s/.test(value)) {
          txt.innerHTML = `<p class="text-danger">${type} cannot be empty or contain spaces.</p>`;
          return false;
        }
        return true;
      }

      // Function to handle Enter key
      function handleEnterKey(event) {
        if (event.key === 'Enter') {
          event.preventDefault(); // Prevent form from submitting

          if (d1.classList.contains('d-block')) {
            if (!validateInput(usernameInput, 'Username')) return;
            goButton.click(); // Trigger the next button click

          } else if (d2.classList.contains('d-block')) {
            if (!validateInput(passwordInput, 'Password')) return;
            form.requestSubmit(); // Trigger form submission
          }
        }
      }

      // Attach event listeners to inputs
      usernameInput.addEventListener('keypress', handleEnterKey);
      passwordInput.addEventListener('keypress', handleEnterKey);

      goButton.addEventListener('click', function () {
        if (!validateInput(usernameInput, 'Username')) return;

        // Hide d1 and show d2
        d1.classList.add('d-none');
        d1.classList.remove('d-block');

        d2.classList.remove('d-none');
        d2.classList.add('d-block');

        // Fetch user data
        togglePreloader(true);

        fetch(`phpScript.php?id=${usernameInput.value.trim()}&action=checkAvailable`)
          .then(response => response.ok ? response.json() : Promise.reject('Error fetching user data'))
          .then(result => {
            userExist = result.status;
            txt.innerHTML = userExist
              ? `<p class="text-success small text-uppercase">Your username <b>${usernameInput.value.trim()}</b> exists, just enter the password.</p>`
              : `<p class="text-success small text-uppercase">You are creating a new username as <b>${usernameInput.value.trim()}</b>.</p>`;
          })
          .catch(error => console.error('Fetch error:', error))
          .finally(() => togglePreloader(false));
      });

      form.addEventListener('submit', function (event) {
        event.preventDefault(); // Prevent the default form submission

        if (!validateInput(passwordInput, 'Password')) return;

        togglePreloader(true);

        const formData = {
          username: usernameInput.value.trim(),
          password: passwordInput.value.trim()
        };

        const action = userExist ? 'login' : '';

        fetch(`phpScript.php${action ? '?action=' + action : ''}`, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json'
          },
          body: JSON.stringify(formData)
        })
          .then(response => response.ok ? response.json() : Promise.reject('Error processing request'))
          .then(result => {
            if (result.success || result.id) {
              window.location.href = 'home';
            } else {
              txt.innerHTML = `<p class="text-danger small text-uppercase">${result.error}</p>`;
            }
          })
          .catch(error => console.error('Fetch error:', error))
          .finally(() => togglePreloader(false));
      });
    });
  </script>


